    private void validateEntry(){
        if(DonorName.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "Please enter Donor Name", "Empty Input",JOptionPane.ERROR_MESSAGE);
        else{
            if(Age.getText().isEmpty())
                JOptionPane.showMessageDialog(this, "Please enter Age", "Empty Input",JOptionPane.ERROR_MESSAGE);
            else{
                if(CurrentAddress.getText().isEmpty())
                    JOptionPane.showMessageDialog(this, "Please enter Current Address", "Empty Input",JOptionPane.ERROR_MESSAGE);
                else{
                    if(City.getText().isEmpty())
                        JOptionPane.showMessageDialog(this,"Please enter City" , "Empty Input",JOptionPane.ERROR_MESSAGE);
                    else{
                        if(BloodGrp.getSelectedIndex()==0)
                            JOptionPane.showMessageDialog(this,"Please select Blood Group" , "Empty Input",JOptionPane.ERROR_MESSAGE);
                        else{
                            if(Mobile.getText().isEmpty())
                                JOptionPane.showMessageDialog(this,"Please enter Mobile no." , "Empty Input",JOptionPane.ERROR_MESSAGE);
                            else{
                                if(Male.isSelected()||Female.isSelected()){
                                    sendDataToDatabase();
                                }
                                else{
                                    JOptionPane.showMessageDialog(this,"Please select Gender" , "Empty Input",JOptionPane.ERROR_MESSAGE);
                                }
                            }
                        }
                    }
                }
            }
        }
    }
    private void sendDataToDatabase(){
            
            String DName =DonorName.getText();
            int age = Integer.parseInt(Age.getText());
            String CrntAdrs=CurrentAddress.getText();
            String city = City.getText();
            String BldGrp = BloodGrp.getSelectedItem().toString();
            long Mob = Long.parseLong(Mobile.getText());
            String gender = null;
            if(Male.isSelected())
                gender="Male";
            else if(Female.isSelected())
                gender="Female";
            try{
                String sql = "Insert into bloodtable(DonorName,CurrentAddress,BloodGroup,MobileNumber,Age,City,Gender) values('"+DName+"','"+CrntAdrs+"','"+BldGrp+"','"+Mob+"','"+age+"','"+city+"','"+gender+"')";
                PreparedStatement st = DbConnection.getConnection(sql);
                st.executeUpdate(sql);
                clearForm();
                JOptionPane.showMessageDialog(null,"Hello, "+DName+" your data is inserted successfully");
                
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(null, e);
            }
    }
