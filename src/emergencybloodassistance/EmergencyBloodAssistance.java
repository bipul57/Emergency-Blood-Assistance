    private void loadPacketsAvailableTable(){
        int c1 = 0,c2 = 0,c3 = 0,c4 = 0,c5 = 0,c6 = 0,c7 = 0,c8 = 0;
        DefaultTableModel dtm=(DefaultTableModel) PacketsAvailableTable.getModel();
        dtm.setRowCount(0);
        try {
            String sql="SELECT * FROM bloodtable";
            PreparedStatement ps=DbConnection.getConnection(sql);
            ResultSet rs=ps.executeQuery(sql);
            while(rs.next()){
                if(rs.getString("BloodGroup").equals("A+"))     c1++;
                if(rs.getString("BloodGroup").equals("A-"))     c2++;
                if(rs.getString("BloodGroup").equals("B+"))     c3++;
                if(rs.getString("BloodGroup").equals("B-"))     c4++;
                if(rs.getString("BloodGroup").equals("AB+"))     c5++;
                if(rs.getString("BloodGroup").equals("AB-"))     c6++;
                if(rs.getString("BloodGroup").equals("O+"))     c7++;
                if(rs.getString("BloodGroup").equals("O-"))     c8++;    
            }
            
            dtm.addRow(new Object[]{"A+",c1});
            dtm.addRow(new Object[]{"A-",c2});
            dtm.addRow(new Object[]{"B+",c3});
            dtm.addRow(new Object[]{"B-",c4});
            dtm.addRow(new Object[]{"AB+",c5});
            dtm.addRow(new Object[]{"AB-",c6});
            dtm.addRow(new Object[]{"O+",c7});
            dtm.addRow(new Object[]{"O-",c8});
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
