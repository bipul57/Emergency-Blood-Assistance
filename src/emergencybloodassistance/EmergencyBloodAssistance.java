    private void Search(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Search

        if(BloodGroupSearch.getSelectedIndex()==0)
        JOptionPane.showMessageDialog(this, "Please Select Blood Group", "Invalid Query",JOptionPane.ERROR_MESSAGE);
        else{
            DefaultTableModel dtm=(DefaultTableModel)SearchDataTable.getModel();
            dtm.setRowCount(0);
            try {
                String searchQuery="SELECT * FROM bloodtable WHERE BloodGroup='"+BloodGroupSearch.getSelectedItem().toString()+"'";
                PreparedStatement ps=DbConnection.getConnection(searchQuery);
                ResultSet rs=ps.executeQuery(searchQuery);
                while(rs.next())
                dtm.addRow(new Object[]{rs.getString("DonorName"),rs.getInt("Age"),rs.getString("City"),rs.getString("Gender"),rs.getBigDecimal("MobileNumber")});
                if(dtm.getRowCount()==0)
                JOptionPane.showMessageDialog(this,"Blood not available for this group at this moment");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_Search

