    public void Loadata(){
        DefaultTableModel dtm=(DefaultTableModel)AdminDataTable.getModel();
            dtm.setRowCount(0);
            try {
                String searchQuery="SELECT * FROM bloodtable";
                PreparedStatement ps=DbConnection.getConnection(searchQuery);
                ResultSet rs=ps.executeQuery(searchQuery);
                while(rs.next())
                dtm.addRow(new Object[]{rs.getInt("SerialNumber"),rs.getString("DonorName"),rs.getInt("Age"),rs.getString("City"),rs.getString("Gender"),rs.getBigDecimal("MobileNumber"),rs.getString("BloodGroup"),rs.getString("CurrentAddress")});
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
    }
