    private void jbtndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtndeleteActionPerformed
        DefaultTableModel dtm=(DefaultTableModel)AdminDataTable.getModel();
          try{
            if (AdminDataTable.getSelectedRow() != -1)
            {
                String DeleteQuery = "DELETE FROM bloodtable WHERE SerialNumber = ? ";
                PreparedStatement ps=DbConnection.getConnection(DeleteQuery);
                int serialNumber = (int)dtm.getValueAt(AdminDataTable.getSelectedRow(), 0);
                ps.setInt(1, serialNumber);
                if(ps.executeUpdate()!=0)
                {
                    dtm.removeRow(AdminDataTable.getSelectedRow());
                    JOptionPane.showMessageDialog(null, "Selected row deleted successfully");
                }
                
            }
            
            } catch (SQLException ex) { 
            Logger.getLogger(AdminSupport.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_jbtndeleteActionPerformed

